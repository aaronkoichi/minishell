int	ft_isspace(int c)
{
	unsigned char	a;

	a = (unsigned char)c;
	if (a == '\t' || a == '\n' || a == '\v' || a == '\f' || a == '\r'
		|| a == ' ')
		return (1);
	return (0);
}
